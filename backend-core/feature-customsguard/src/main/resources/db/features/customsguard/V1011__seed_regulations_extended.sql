# กฎว่าด้วยถิ่นกำเนิดสินค้า (Rules of Origin) — วิธีคำนวณ

ข้อ 1 กฎว่าด้วยถิ่นกำเนิดสินค้า (Rules of Origin: ROO) เป็นหลักเกณฑ์ที่กำหนดว่าสินค้ามีถิ่นกำเนิดในประเทศใด เพื่อใช้ประโยชน์จากการลดอากรตาม FTA ผู้นำเข้าต้องพิสูจน์ว่าสินค้ามีถิ่นกำเนิดในประเทศคู่สัญญา FTA

ข้อ 2 เกณฑ์การพิจารณาถิ่นกำเนิดสินค้า 4 แบบหลัก:

1. Wholly Obtained (WO) — สินค้าที่ได้มาทั้งหมดในประเทศ:
- ผลิตผลเกษตร: พืช สัตว์ ที่เพาะปลูก/เลี้ยงในประเทศ
- แร่ธาตุ: ที่สกัดจากดิน/น้ำในประเทศ
- สัตว์น้ำ: จับจากเรือที่จดทะเบียนในประเทศ
- ของเสียจากกระบวนการผลิตในประเทศ

2. Regional Value Content (RVC) — มูลค่าเนื้อหาของภูมิภาค:
สูตรคำนวณ:
RVC (%) = (FOB Price - Non-originating Materials) / FOB Price x 100
หรือ
RVC (%) = (Originating Materials + Direct Labour + Direct Overhead + Profit) / FOB Price x 100

ตัวอย่าง: สินค้า FOB 1,000 USD ใช้วัตถุดิบไม่ได้ถิ่นกำเนิด 500 USD
RVC = (1,000 - 500) / 1,000 x 100 = 50% → ผ่านเกณฑ์ RVC 40%

3. Change in Tariff Classification (CTC) — การเปลี่ยนพิกัดอัตราศุลกากร:
(ก) CC (Change in Chapter): เปลี่ยนตอน 2 หลัก — เข้มงวดที่สุด
(ข) CTH (Change in Tariff Heading): เปลี่ยนประเภท 4 หลัก — ทั่วไป
(ค) CTSH (Change in Tariff Sub-Heading): เปลี่ยนประเภทย่อย 6 หลัก — ยืดหยุ่นที่สุด

ตัวอย่าง CTH: วัตถุดิบ HS 5205 (ด้ายฝ้าย) → สินค้าสำเร็จรูป HS 6109 (เสื้อยืด)
เปลี่ยนจากตอน 52 → ตอน 61 → ผ่านเกณฑ์ CTH

4. Specific Process (SP) — กระบวนการเฉพาะ:
- สินค้าบางรายการกำหนดกระบวนการผลิตเฉพาะ เช่น สิ่งทอต้อง Double Transformation
- เคมีภัณฑ์ต้องผ่านปฏิกิริยาเคมี (Chemical Reaction Rule)

ข้อ 3 De Minimis Rule (หลักส่วนน้อยที่ยกเว้น):
- วัตถุดิบที่ไม่ได้ถิ่นกำเนิด ไม่เกินร้อยละ 10 ของ FOB ไม่ต้องนำมาคำนวณ
- ยกเว้นสินค้าเกษตร สิ่งทอ (บาง FTA) ที่มีเกณฑ์ De Minimis ต่างออกไป

ข้อ 4 Cumulation (การสะสมถิ่นกำเนิด):
- Bilateral Cumulation: นับวัตถุดิบจากประเทศคู่สัญญา FTA เท่านั้น
- Diagonal Cumulation: นับวัตถุดิบจากประเทศที่มี FTA เดียวกัน (เช่น RCEP 15 ประเทศ)
