# หลักเกณฑ์การนำเข้าของชั่วคราว (Temporary Admission)

ข้อ 1 การนำเข้าของชั่วคราว คือ การนำสินค้าเข้ามาในราชอาณาจักรโดยมีวัตถุประสงค์จะส่งกลับออกไปภายในระยะเวลาที่กำหนด โดยได้รับยกเว้นหรือลดหย่อนอากรขาเข้า

ข้อ 2 ประเภทของที่สามารถนำเข้าชั่วคราวได้:
(ก) ของที่นำเข้าเพื่อแสดงในงานนิทรรศการ งานแสดงสินค้า การประชุมสัมมนา
(ข) เครื่องมือ อุปกรณ์ เครื่องจักร ที่นำเข้าเพื่อใช้ในโครงการก่อสร้างหรือซ่อมบำรุงชั่วคราว
(ค) ตัวอย่างสินค้าเพื่อการเจรจาการค้า (Commercial Samples)
(ง) อุปกรณ์วิชาชีพ เช่น กล้องถ่ายภาพ เครื่องมือแพทย์ อุปกรณ์กีฬาสำหรับการแข่งขัน
(จ) ภาชนะบรรจุสินค้า (Containers) ที่จะส่งกลับหลังขนถ่ายสินค้า

ข้อ 3 ขั้นตอนการดำเนินพิธีการ:
(1) ยื่นคำร้องขอนำเข้าชั่วคราวต่อด่านศุลกากร พร้อมเอกสาร Invoice, Packing List
(2) วางหลักประกัน (Bond/Guarantee) เท่ากับค่าอากรที่พึงต้องชำระ หรือใช้ ATA Carnet
