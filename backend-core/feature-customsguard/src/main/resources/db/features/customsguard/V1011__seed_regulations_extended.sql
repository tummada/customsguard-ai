# เขตปลอดอากร (Free Zone) และคลังสินค้าทัณฑ์บน (Bonded Warehouse)

ข้อ 1 เขตปลอดอากร (Free Zone) คือ เขตพื้นที่ที่กำหนดให้เป็นเขตพิเศษทางศุลกากร สินค้าที่นำเข้ามาในเขตปลอดอากรได้รับยกเว้นอากรขาเข้า ภาษีสรรพสามิต และภาษีมูลค่าเพิ่ม จนกว่าจะนำสินค้าออกจากเขตปลอดอากรเพื่อใช้ภายในประเทศ

ข้อ 2 ประเภทเขตปลอดอากร:
(ก) เขตปลอดอากรเพื่อการอุตสาหกรรม (Export Processing Zone - EPZ): สำหรับผู้ผลิตที่ส่งออกเป็นหลัก สินค้าที่ผลิตได้ต้องส่งออกไม่น้อยกว่าร้อยละ 80
(ข) เขตปลอดอากรเพื่อพาณิชยกรรม: สำหรับการจัดเก็บ กระจายสินค้า บรรจุหีบห่อ
(ค) เขตปลอดอากรท่าอากาศยานสุวรรณภูมิ: สำหรับสินค้าขนส่งผ่าน (Transit) และสินค้าถ่ายลำ (Transshipment)

