# หลักเกณฑ์การกำหนดราคาศุลกากร (Customs Valuation)

ข้อ 1 การกำหนดราคาศุลกากรเพื่อใช้เป็นฐานในการคำนวณอากรขาเข้า ให้เป็นไปตามความตกลงว่าด้วยการกำหนดราคาศุลกากร (WTO Customs Valuation Agreement) โดยใช้วิธีการ 6 วิธี ตามลำดับดังนี้:

วิธีที่ 1 ราคาซื้อขายของที่นำเข้า (Transaction Value): ใช้ราคาที่ผู้ซื้อจ่ายจริงหรือที่จะต้องจ่าย (Price Actually Paid or Payable) สำหรับสินค้าที่ขายเพื่อส่งออกมายังประเทศไทย บวกด้วยค่าใช้จ่ายที่ต้องรวม เช่น ค่านายหน้า ค่าบรรจุหีบห่อ ค่าลิขสิทธิ์

วิธีที่ 2 ราคาซื้อขายของที่เหมือนกัน (Identical Goods): ใช้ราคาซื้อขายของสินค้าที่เหมือนกันทุกประการ ที่ส่งออกมายังประเทศไทยในเวลาใกล้เคียงกัน

วิธีที่ 3 ราคาซื้อขายของที่คล้ายกัน (Similar Goods): ใช้ราคาซื้อขายของสินค้าที่คล้ายคลึงกัน ทำหน้าที่ทดแทนกันได้ในทางพาณิชย์

วิธีที่ 4 ราคาหักทอน (Deductive Value): ใช้ราคาขายภายในประเทศ หักค่าใช้จ่ายที่เกิดขึ้นภายในประเทศออก เช่น กำไร ค่าขนส่งภายใน ค่าอากร

วิธีที่ 5 ราคาคำนวณ (Computed Value): ใช้ต้นทุนการผลิตในประเทศผู้ส่งออก บวกกำไรและค่าใช้จ่ายทั่วไป

วิธีที่ 6 ราคากำหนด (Fallback Method): ใช้วิธีอื่นที่สอดคล้องกับหลักการของ WTO โดยยืดหยุ่นจากวิธีที่ 1-5

ข้อ 2 ราคาศุลกากร (Customs Value) คือราคา CIF ซึ่งประกอบด้วย:
- Cost: ราคาสินค้า (FOB)
- Insurance: ค่าประกันภัย (ถ้าไม่ระบุ ใช้ร้อยละ 1 ของราคา FOB)
- Freight: ค่าขนส่งระหว่างประเทศถึงท่าเรือ/สนามบินปลายทางในประเทศไทย

ข้อ 3 ค่าใช้จ่ายที่ต้องรวมในราคาศุลกากร:
(ก) ค่านายหน้า (Buying Commission ไม่รวม, Selling Commission รวม)
(ข) ค่าภาชนะบรรจุและค่าบรรจุหีบห่อ
(ค) ค่าลิขสิทธิ์และค่าสิทธิบัตร (Royalties) ที่ผู้ซื้อต้องจ่ายเป็นเงื่อนไขของการขาย
(ง) มูลค่าของวัตถุดิบ เครื่องมือ แม่พิมพ์ที่ผู้ซื้อจัดส่งให้ผู้ขายฟรีหรือในราคาต่ำ (Assists)

ข้อยกเว้น:
- ค่าขนส่งภายในประเทศไทย (หลังผ่านด่านศุลกากร) ไม่ต้องรวม
