# การจัดทำใบขนสินค้าขาเข้า (Import Declaration) — ขั้นตอนโดยละเอียด

ข้อ 1 ใบขนสินค้าขาเข้า คือ เอกสารที่ผู้นำเข้าหรือตัวแทนออกของ (Licensed Customs Broker) จัดทำขึ้นเพื่อสำแดงรายละเอียดสินค้าต่อกรมศุลกากร เพื่อผ่านพิธีการนำเข้าและชำระอากร

ข้อ 2 ขั้นตอนการจัดทำใบขนสินค้าขาเข้า:
ขั้นตอนที่ 1: รวบรวมเอกสาร
- Invoice จากผู้ขายต่างประเทศ (ระบุราคา FOB/CIF สกุลเงิน Incoterms)
- Packing List (รายละเอียดจำนวน น้ำหนัก ขนาดหีบห่อ)
- Bill of Lading (B/L) หรือ Air Waybill (AWB)
- Certificate of Origin (CO/Form E/Form D) กรณีใช้สิทธิ FTA
- ใบอนุญาตนำเข้า (LPI) กรณีสินค้าควบคุม

ขั้นตอนที่ 2: จำแนกพิกัดอัตราศุลกากร (HS Code Classification)
- ตรวจสอบพิกัดอัตราศุลกากร (HS Code) ตาม AHTN 2022
- ตรวจสอบอัตราอากร (MFN Rate, FTA Rate)
- ตรวจสอบว่าเป็นสินค้าควบคุม (LPI) หรือไม่

ขั้นตอนที่ 3: คำนวณค่าอากร
- ราคาศุลกากร = CIF Value (ราคาสินค้า + ค่าประกัน + ค่าขนส่ง)
- อากรขาเข้า = ราคาศุลกากร x อัตราอากร
- ภาษีมูลค่าเพิ่ม (VAT) = (ราคาศุลกากร + อากรขาเข้า) x 7%
- ภาษีสรรพสามิต (ถ้ามี) = ตามอัตราที่กรมสรรพสามิตกำหนด

ขั้นตอนที่ 4: กรอกข้อมูลในระบบ e-Customs
